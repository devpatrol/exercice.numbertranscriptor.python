def operation(caractere):
    if caractere == "(":
        return " factor "
    if caractere == "+":
        return " plus  "
    if caractere == "-":
        return " minus "
    if caractere == "/":
        return " divided by "
    if caractere == "*":
        return " times "
    if caractere == "%":
        return " modulo "
    if caractere == "=":
        return " equal "
    if caractere == ".":
        return " point "